import Foundation

class CustomNotif {

    var body: String
    var timestamp: String
    var type: String?
    var title: String

    init(body: String, timestamp: String, type: String, title: String) {
        self.body = body
        self.timestamp = timestamp
        self.type = type
        self.title = title
    }





}
